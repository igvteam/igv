    private void onChange() {
        try {
            ArrayList<Double> inputs = new ArrayList<>();
            inputs.add(Double.parseDouble(minText.getText()));
            inputs.add(Double.parseDouble(midText.getText()));
            inputs.add(Double.parseDouble(maxText.getText()));
            inputs.sort(Comparator.naturalOrder());
            if (useDoubleGradientCheckBox.isSelected()) {
                colorScale = new ContinuousColorScale(
                        inputs.get(0),
                        inputs.get(1),
                        inputs.get(2),
                        minColor.getSelectedColor(),
                        midColor.getSelectedColor(),
                        maxColor.getSelectedColor());
            } else {
                colorScale = new ContinuousColorScale(Math.min(Double.parseDouble(minText.getText()), Double.parseDouble(maxText.getText())),
                        Math.max(Double.parseDouble(minText.getText()), Double.parseDouble(maxText.getText())),
                        minColor.getSelectedColor(),
                        maxColor.getSelectedColor());
            }
        } catch (NumberFormatException numberFormatException) {
            //
        }
        displayPanel.updateColorScale(colorScale);
    }

    private void onOK() {
        try {
            Double.parseDouble(minText.getText());
            Double.parseDouble(minText.getText());
            Double.parseDouble(minText.getText());
            onChange();
            dispose();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Limit fields must be numeric.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void onCancel() {
        isCanceled = true;
        dispose();
    }

